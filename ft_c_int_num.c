#include "libft.h"

int	ft_c_int_num(int n)
{
	int count;

	if (n == -2147483648)
	{
		count = 11;
		return (count);
	}
	if (n <= 0)
		count = 1;
	else
		count = 0;
	while (n != 0)
	{
		n = n / 10;
		count++;
	}
	return (count);
}
