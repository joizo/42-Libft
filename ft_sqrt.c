#include "libft.h"

int	ft_sqrt(int num)
{
	int i;
	int b;

	if (!num || num <= 0)
		return (0);
	i = 1;
	b = i * i;
	while (b <= num)
	{
		if (b < num)
			i++;
		if (b == num)
			return (1);
		b = i * i;
	}
	return (0);
}
